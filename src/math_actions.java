public class math_actions {

    public static float addition_operation(float input_1, float input_2){

        return (input_1 + input_2);

    }

    public static float subtraction_operation(float input_1, float input_2){

        return (input_1 - input_2);

    }


    public static float multiplication_operation(float input_1, float input_2){

        return (input_1 * input_2);

    }

    public static float division_operation(float input_1, float input_2){

        return (input_1 / input_2);

    }

}
